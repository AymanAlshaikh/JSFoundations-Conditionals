// Define the numbers and the operation here
const numOne = 10;
const numTwo = 7;
let operator = "-";
// Write if-statements here
if (operator === "+"){
    console.log("addition")
    console.log(numOne + numTwo)
}
else if (operator === "-"){
    console.log("subtraction")
    console.log(numOne - numTwo)
}
else if (operator === "*"){
    console.log("Multiplication")
    console.log(numOne * numTwo)
}
else{
    console.log("Division")
    console.log(numOne / numTwo)
}
